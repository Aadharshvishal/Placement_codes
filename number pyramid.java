import java.util.*;
public class main{
    public static void main(String[] args){
        Scanner s=new Scanner(System.in);
        System.out.println("Enter rows:");
        int rows=s.nextInt();
        for(int i=1;i<=rows;i++){
            for(int j=1;j<=1;j++){
                System.out.print(i+" ");
            }
            System.out.println();
        }
    }
}